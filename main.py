def enforce_neri_pronouns(text: str) -> str:
    t = (text or "").strip()
    if not t:
        return t
    for pattern, repl in FEM_TO_MASC_REPLACEMENTS:
        t = re.sub(pattern, repl, t, flags=re.IGNORECASE)
    return t

