def _clean_name_token(s: str) -> str:
    s = (s or "").strip().lower()
    s = s.replace("‚Äô", "'").replace(" º", "'")
    s = re.sub(r"^[^\w–∞-—â—å—é—è—î—ñ—ó“ë\-']+|[^\w–∞-—â—å—é—è—î—ñ—ó“ë\-']+$", "", s, flags=re.IGNORECASE)
