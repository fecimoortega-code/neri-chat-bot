def _one(seq: list[str]) -> str:
    return random.choice(seq)

def _dedupe_join(parts: list[str]) -> str:
    out = []
    seen = set()
    for p in parts:
        p = (p or "").strip()
        if not p:
            continue
        key = p.lower()
        if key in seen:
            continue
        seen.add(key)
        out.append(p)
    return " ".join(out).strip()

